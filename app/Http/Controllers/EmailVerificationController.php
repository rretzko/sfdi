     * @param  \Illuminate\Http\Request  $request
     * @param  string $token
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $token)
    {
        $email = \App\Email::where('verified', $token)->first();
        
        if($email){
            
            $email->verified = '1';
            $email->save();
            Session::flash('success','Thanks! Your email has been verified.');
            
        }else{
            
            Session::flash('warning', "Sorry, we couldn't find that email.");
        }
        
        return view('auth.login');
    }

